def handle_help(reply_token: str, text: str) -> bool:
    # 精確匹配分類查詢
    m = re.match(r"^(?:!)?指令\s+(.+)", text)
    if m:
        cat = m.group(1).strip()
        # 支援別名
        aliases = {
            "家事": "家事", "家務": "家事", "chores": "家事",
            "點數": "家事", "分數": "家事", "points": "家事",
            "購物": "購物", "shopping": "購物", "買": "購物",
            "記帳": "記帳", "accounting": "記帳", "帳": "記帳",
            "罰款": "罰款", "fine": "罰款", "欠款": "罰款",
            "待辦": "待辦", "todo": "待辦", "提醒": "待辦",
            "斷捨離": "斷捨離", "declutter": "斷捨離",
            "書櫃": "書櫃", "書": "書櫃", "book": "書櫃", "books": "書櫃", "閱讀": "書櫃",
            "生活": "生活", "tool": "生活", "工具": "生活", "utils": "生活",
            "進階": "進階", "advanced": "進階", "查詢": "進階",
            "語言": "語言", "language": "語言", "日文": "語言", "西文": "語言",
            "趣味": "趣味", "fun": "趣味", "遊戲": "趣味", "game": "趣味",
            "全部": "全部", "all": "全部", "完整": "全部",
            "常用": "常用", "常用指令": "常用", "常用功能": "常用",
        }
        target = aliases.get(cat, cat)
        content = _HELP_CATEGORIES.get(target)
        if content:
            reply(reply_token, content)
        else:
            reply(reply_token, f"找不到「{cat}」這個分類。\n\n可用分類：家事、購物、記帳、罰款、待辦、斷捨離、書櫃、生活、進階、語言、趣味\n或傳「!指令」看常用指令。")
        return True

    # 預設常用指令
    if text in ["說明", "幫助", "功能", "help", "指令", "指令清單", "清單"]:
        reply(reply_token, _HELP_CATEGORIES["常用"])
        return True
    return False
